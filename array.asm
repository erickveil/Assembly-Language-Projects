EXTRN	GETDEC$:FAR
EXTRN	PUTDEC$:FAR
EXTRN	PUTBIN:FAR
EXTRN	PUTOCT:FAR
EXTRN	PUTSTRNG:FAR
EXTRN	NEWLINE:FAR
EXTRN	PAUSE:FAR
EXTERN	CLEAR:FAR

PAGE		80,132
.MODEL		SMALL,BASIC
.STACK		64
.FARDATA	DSEG
	MON_STRT	db	0,8,17,23,29
	MONTH		DB	'January$February$March$April$May'
	TXT			DB	0
	
.CODE 
	ASSUME	DS:DSEG,ES:DSEG
MAIN	PROC	FAR
	MOV		AX,	SEG	DSEG
	MOV		DS,AX
	MOV		ES,AX
	
	
	mov	ax,3
	push ax
	call GET_STR_STRT
	
	
	push	ax
	jmp BAAH
	call 	PRINT_MON
	BRR:
	call	NEWLINE
	call	NEWLINE
	jmp YAA
	
	BAAH:
	pop	ax
	mov	dx,OFFSET MONTH
	add	dx,ax
	mov	ah,09
	int	21H
	jmp	BRR
	YAA:
	
.EXIT
MAIN	ENDP

PRINT_MON	PROC	NEAR PUBLIC SUBSCRIPT:WORD
	PUSHF

	mov	dx,OFFSET MONTH
	add	dx,SUBSCRIPT
	mov	ah,09
	int	21H
	
	RET
	
PRINT_MON	ENDP

GET_STR_STRT	PROC	NEAR PUBLIC SUBSCRIPT:WORD
	PUSHF
	
	mov	di,SUBSCRIPT
	mov	al,[MON_STRT+di]
	cbw
	
	POPF
	RET
GET_STR_STRT	ENDP

END		MAIN